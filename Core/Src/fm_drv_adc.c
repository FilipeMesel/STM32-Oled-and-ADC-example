void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	ADC.uValue[ADC_CHANNEL_01] = HAL_ADC_GetValue(&hadc1);
	ADC.uValue[ADC_CHANNEL_02] = HAL_ADC_GetValue(&hadc2);
}

